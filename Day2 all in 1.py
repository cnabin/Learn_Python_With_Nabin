# String
# Empty String : ""
# String with single element = "H"
my_string = "hello"
print(my_string)


# list -> Mutable datatype , ['Hello',12,23.04,[10,12,'Hello World!']]
# Empty list=[]
# list with single item=["Hello"]
my_list = [1, 2, 3, "apple", "banana"]
print(my_list)


# tuple -> Immutable datatype, ('Hello',112, 23.2,[1,2,3],(4,5,6))
# Empty Tuple = ()
# Tuple with single item = ("Hello",)
my_tuple = (1, 2, 3, "apple", "banana")
print(my_tuple)

